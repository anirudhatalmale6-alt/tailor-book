export default function BottomNav() {
  const pathname = usePathname();

  function isActive(href: string) {
    if (href === '/') {
      return pathname === '/' || (pathname.startsWith('/customers') && !pathname.startsWith('/customersX'));
    }
    return pathname.startsWith(href);
  }

  return (
    <nav className="fixed bottom-0 left-0 right-0 bg-royal-bg border-t border-royal-border z-50">
      <div className="max-w-lg mx-auto flex justify-around items-center h-16">
        {tabs.map((tab) => {
          const active = isActive(tab.href);
          return (
            <Link
              key={tab.href}
              href={tab.href}
              className="flex flex-col items-center justify-center flex-1 py-1"
            >
              {tab.icon(active)}
              <span
                className={`text-[10px] mt-0.5 ${
                  active ? 'text-gold font-semibold' : 'text-white/70'
                }`}
              >
                {tab.label}
              </span>
            </Link>
          );
        })}
      </div>
    </nav>
  );
}
